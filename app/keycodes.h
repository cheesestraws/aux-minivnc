#ifndef KEYCODES_H
#define KEYCODES_H

/* These are ADB virtual keycodes, lifted from IM: TB Essentials ch 2 */

#define COMMAND 55
#define SHIFT 	56
#define LOCK 	57
#define OPTION 	58
#define CTRL 	59


#define  KP_DECIMAL	 0x41
#define  KP_MULTIPLY 0x43
#define  KP_PLUS	 0x45
#define  KP_CLEAR	 0x47
#define  KP_DIVIDE	 0x4B
#define  KP_ENTER	 0x4C
#define  KP_MINUS	 0x4E
#define  KP_EQUALS	 0x51
#define  KP_0		 0x52

#endif